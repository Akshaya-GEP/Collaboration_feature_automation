// ─────────────────────────────────────────────────────────────────────────────
// Load conversation turns from JSON file.
// JSON contains only user queries (userMessage or userQuery). expectedIntent is
// generated by LLM (JUDGE_API_*) when missing. Validation rules come from
// validationConfig (common file), not from the scenario or from this JSON.
// ─────────────────────────────────────────────────────────────────────────────

Given('I load conversation turns from {string}', async function (this: CustomWorld, filePath: string) {
    const baseDir = process.cwd().includes('playwright_automation')
        ? process.cwd()
        : path.join(process.cwd(), 'playwright_automation');
    const resolvedPath = path.resolve(baseDir, filePath);
    const jsonContent = fs.readFileSync(resolvedPath, 'utf-8');
    const data = JSON.parse(jsonContent) as {
        selectors?: { userQuerySelector?: string; agentResponseSelector?: string };
        turns: Array<{
            userMessage?: string;
            userQuery?: string;
            expectedResponse?: string;
            matchType?: string;
            expectedIntent?: string;
            minKeywords?: number;
        }>;
    };
    assert(Array.isArray(data.turns) && data.turns.length > 0, `JSON must have a non-empty "turns" array: ${filePath}`);

    const turnsWithIntent: Array<{
        userMessage: string;
        expectedResponse: string;
        matchType: string;
        expectedIntent?: string;
        minKeywords?: number;
    }> = [];

    for (const t of data.turns) {
        const userMessage = (t.userMessage ?? t.userQuery ?? '').trim();
        assert(userMessage, `Each turn must have "userMessage" or "userQuery": ${filePath}`);

        let expectedIntent = t.expectedIntent?.trim();
        if (!expectedIntent) {
            console.log(`  Generating expectedIntent via LLM for: "${userMessage.substring(0, 50)}..."`);
            expectedIntent = await generateExpectedIntent(userMessage);
            console.log(`  → expectedIntent: "${expectedIntent.substring(0, 60)}..."`);
        }

        turnsWithIntent.push({
            userMessage,
            expectedResponse: (t.expectedResponse ?? expectedIntent).trim(),
            matchType: (t.matchType ?? 'semantic').trim().toLowerCase(),
            expectedIntent,
            minKeywords: t.minKeywords,
        });
    }

    this.loadedTurns = turnsWithIntent;
    this.loadedSelectors = data.selectors
        ? {
            userQuerySelector: data.selectors.userQuerySelector ?? VALIDATION_RULES.userQuerySelector,
            agentResponseSelector: data.selectors.agentResponseSelector ?? VALIDATION_RULES.agentResponseSelector,
        }
        : {
            userQuerySelector: VALIDATION_RULES.userQuerySelector,
            agentResponseSelector: VALIDATION_RULES.agentResponseSelector,
        };
    this.loadedMinScore = VALIDATION_RULES.minScorePerTurn;
});

// ─────────────────────────────────────────────────────────────────────────────
// Run loaded turns and enforce minimum score
// ─────────────────────────────────────────────────────────────────────────────

Then('the agent conversation should match the loaded turns and meet minimum score {int}', async function (this: CustomWorld, minScore: number) {
    assert(this.page, 'World.page was not initialized');
    assert(this.loadedTurns?.length, 'No loaded turns. Use "I load conversation turns from ..." first.');

    const turns: TurnExpectation[] = this.loadedTurns.map((t) => ({
        userMessage: t.userMessage,
        expectedResponse: t.expectedResponse,
        matchType: (t.matchType as MatchType) || 'contains',
        expectedIntent: t.expectedIntent,
        minKeywords: t.minKeywords,
    }));

    const selectors: ChatSelectors | undefined = this.loadedSelectors
        ? {
            userQuerySelector: this.loadedSelectors.userQuerySelector,
            agentResponseSelector: this.loadedSelectors.agentResponseSelector,
        }
        : undefined;

    const result = await runMultiTurnConversationAndValidate(this.page!, turns, selectors);

    if (!result.passed && result.turnIndex !== undefined && result.results[result.turnIndex]) {
        const r = result.results[result.turnIndex];
        throw new Error(
            `Multi-turn validation failed at turn ${result.turnIndex + 1}. ` +
            `User: "${r.userMessage}". Expected: "${r.expectedResponse}". ` +
            `Actual: "${r.actualResponse.substring(0, 200)}${r.actualResponse.length > 200 ? '...' : ''}". ` +
            (r.failureReason ? `Reason: ${r.failureReason}` : '')
        );
    }
    if (!result.passed) {
        throw new Error('Multi-turn validation failed.');
    }

    for (let i = 0; i < result.results.length; i++) {
        const r = result.results[i];
        if (r.score != null && r.score < minScore) {
            throw new Error(
                `Turn ${i + 1} score ${r.score} is below minimum ${minScore}. User: "${r.userMessage}".`
            );
        }
    }
});

